package com.ty.setterinjectionforpdtwrapperstring.beans;

public class Test {
	
	private int x;
	private Double y;
	private String z;

	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public Double getY() {
		return y;
	}
	public void setY(Double y) {
		this.y = y;
	}
	public String getZ() {
		return z;
	}
	public void setZ(String z) {
		this.z = z;
	}
	@Override
	
	public String toString() {
		return "Test [x=" + x + ", y=" + y + ", z=" + z +  "]";
	}

	
	

}
